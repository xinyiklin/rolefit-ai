// Generates src/typeset/metrics.gen.ts from the shipped Latin Modern woff2
// files. Run: node scripts/gen-typeset-metrics.mjs
//
// The typeset engine (src/typeset/) measures text from these committed tables
// instead of the browser or runtime font parsing, so layout is byte-identical
// on every machine and matches what TeX computes from its TFMs. Regenerate only
// if the font files under public/fonts/ change.
//
// Per face we extract, in font units (1000/em):
//   - advances for the engine charset (printable ASCII + Latin-1 letters +
//     typographic punctuation the TeX-ligature layer emits: ﻗ ﻗ ﻗ ﻗ ﻗ ﻗ ﻗ۱ ﺡﺓ ﻗ۵)
//   - kerning pairs (GPOS, ﻗ TeX's TFM kerns for Latin Modern)
//   - f-ligatures (ff fi fl ffi ffl) with the ligature glyph's advance
//   - per-char ink bboxes (yMin/yMax ﻗ TeX's height/depth for strutless rows)
//   - font-wide hhea ascent/descent, x-height, cap-height
import * as fontkitNs from "fontkit";
import { writeFileSync } from "node:fs";

// fontkit is CJS; under Node ESM its API surface lands on the namespace.
const fontkit = fontkitNs.default ?? fontkitNs;

const FACES = {
  regular: "public/fonts/LMRoman10-Regular.woff2",
  bold: "public/fonts/LMRoman10-Bold.woff2",
  italic: "public/fonts/LMRoman10-Italic.woff2",
  boldItalic: "public/fonts/LMRoman10-BoldItalic.woff2",
  // Display master: TeX sets bold at sizes ﻗ۴ 12pt (the \Huge/\LARGE/\Large
  // name) from lmbx12, ~2% narrower per glyph than scaled lmbx10.
  boldDisplay: "public/fonts/LMRoman12-Bold.otf",
  // Real small-caps face for \scshape headings (lowercase glyphs ARE small
  // capitals here, with their true advances).
  caps: "public/fonts/LMRomanCaps10-Regular.otf"
};

const ASCII = Array.from({ length: 0x7f - 0x20 }, (_, i) => String.fromCharCode(0x20 + i));
const LATIN1 = Array.from({ length: 0xff - 0xc0 + 1 }, (_, i) => String.fromCharCode(0xc0 + i))
  .concat(["ﺣ", "ﺣ۶", "ﺣﺎ", "ﺣ۸", "ﺣ۷", "ﺣ۹", "ﺣ،", "ﺣ۰", "ﺣ ", "ﺣ۱", "ﺣ۳", "ﺣ­", "ﺣ؛", "ﺣ؟", "ﺣﺁ", "ﺣﺏ", "ﺣﺎ", "ﺣﺑ", "ﺣﭘ", "ﺣﭦ", "ﺣﺗ", "ﺣﭨ", "ﺣﺙ"]);
const TYPO = ["ﻗ", "ﻗ", "ﻗ", "ﻗ", "ﻗ", "ﻗ", "ﻗ۱", "ﺡﺓ", "ﻗ۵"];
const CHARSET = [...new Set([...ASCII, ...LATIN1, ...TYPO])];
const LIGATURES = ["ffi", "ffl", "ff", "fi", "fl"];

function faceTables(path) {
  const font = fontkit.openSync(path);
  const scale = 1000 / font.unitsPerEm;
  const adv = {};
  const bbox = {};
  for (const ch of CHARSET) {
    if (!font.hasGlyphForCodePoint(ch.codePointAt(0))) continue;
    const run = font.layout(ch);
    if (!run.glyphs.length) continue;
    adv[ch] = Math.round(run.positions[0].xAdvance * scale * 100) / 100;
    const b = run.glyphs[0].bbox;
    if (b && Number.isFinite(b.minY) && Number.isFinite(b.maxY)) {
      bbox[ch] = [Math.round(b.minY * scale), Math.round(b.maxY * scale)];
    }
  }
  // Kerning: layout(a+b) width minus solo advances. Only store non-zero pairs.
  const kern = {};
  const kernable = CHARSET.filter((c) => adv[c] !== undefined && c !== " ");
  for (const a of kernable) {
    for (const b of kernable) {
      const run = font.layout(a + b);
      if (run.glyphs.length !== 2) continue; // ligated (e.g. f+i) ﻗ handled as ligature
      const total = run.positions.reduce((s, p) => s + p.xAdvance, 0) * scale;
      const k = Math.round((total - (adv[a] + adv[b])) * 100) / 100;
      if (Math.abs(k) >= 0.5) kern[a + b] = k;
    }
  }
  // f-ligatures: single-glyph advance when the whole cluster ligates.
  const lig = {};
  for (const l of LIGATURES) {
    const run = font.layout(l);
    if (run.glyphs.length === 1) {
      lig[l] = Math.round(run.positions[0].xAdvance * scale * 100) / 100;
    }
  }
  return {
    ascent: Math.round(font.ascent * scale),
    descent: Math.round(font.descent * scale),
    capHeight: Math.round(font.capHeight * scale),
    xHeight: Math.round(font.xHeight * scale),
    adv,
    kern,
    lig,
    bbox
  };
}

const out = {};
for (const [name, path] of Object.entries(FACES)) {
  out[name] = faceTables(path);
  console.log(name, "chars:", Object.keys(out[name].adv).length, "kerns:", Object.keys(out[name].kern).length, "ligs:", Object.keys(out[name].lig).length);
}

const banner = `// GENERATED by scripts/gen-typeset-metrics.mjs ﻗ do not edit by hand.
// Latin Modern Roman metrics in font units (1000/em), extracted from the woff2
// files under public/fonts/. See the script header for what each table holds.

export type FaceMetrics = {
  ascent: number;
  descent: number;
  capHeight: number;
  xHeight: number;
  adv: Record<string, number>;
  kern: Record<string, number>;
  lig: Record<string, number>;
  bbox: Record<string, [number, number]>;
};

export type FaceName = "regular" | "bold" | "italic" | "boldItalic" | "boldDisplay" | "caps";

export const LM_METRICS: Record<FaceName, FaceMetrics> = `;

writeFileSync("src/typeset/metrics.gen.ts", banner + JSON.stringify(out) + " as const;\n");
console.log("wrote src/typeset/metrics.gen.ts");
